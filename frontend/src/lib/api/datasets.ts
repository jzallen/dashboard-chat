/**
 * Datasets API
 */

import { get, patch, post, uploadFile } from "./client";
import type { RAQBTree } from "@/raqb";
import type { DatasetSparse } from "./projects";

export interface FieldConfig {
  label: string;
  type: "text" | "number" | "boolean" | "datetime" | "select";
  operators?: string[];
  listValues?: Array<{ value: string; title: string }>;
  nullable?: boolean;
}

export interface SchemaConfig {
  fields: Record<string, FieldConfig>;
}

export interface Transform {
  id: string;
  name: string;
  description: string | null;
  condition_json: RAQBTree;
  condition_sql: string | null;
  status: 'enabled' | 'disabled' | 'deleted';
}

export interface Dataset {
  id: string;
  project_id: string;
  name: string;
  description: string | null;
  schema_config: SchemaConfig;
  partition_fields: string[];
  transforms: Transform[];
  preview_rows: Record<string, unknown>[];
  staging_sql?: string | null;
}

export interface DatasetUploadResponse extends Dataset {
  preview_rows: Record<string, unknown>[];
}

// Upload Event types for the two-step upload flow

export type UploadStatus = "pending" | "processing" | "completed" | "failed";

export interface UploadEvent {
  id: string;
  project_id: string;
  dataset_id: string | null;
  status: UploadStatus;
  raw_storage_path: string;
  original_filename: string;
  file_size: number;
  row_count: number;
  error_message: string | null;
  created_at: string;
  processed_at: string | null;
  preview_rows: Record<string, unknown>[];
}

export interface DatasetCreateRequest {
  upload_id: string;
  description?: string;
  partition_fields?: string[];
}

export interface DatasetUpdate {
  name?: string;
  description?: string;
}

export interface TransformCreate {
  name: string;
  description?: string;
  condition_json: RAQBTree;  // RAQB JSON tree (for UI rehydration)
  condition_sql: string;     // SQL WHERE clause (generated by raqbToSql)
  nl_prompt?: string;
}

export interface TransformUpdate {
  name?: string;
  description?: string;
  condition_json?: RAQBTree;
  condition_sql?: string;
  status?: 'enabled' | 'disabled' | 'deleted';
}

// Note: AggregatedSqlResponse removed - use Dataset.staging_sql instead

/**
 * List all datasets, optionally filtered by project
 */
export async function listDatasets(projectId?: string): Promise<Dataset[]> {
  const query = projectId ? `?project_id=${projectId}` : "";
  return get<Dataset[]>(`/api/datasets${query}`);
}

/**
 * Get a single dataset by ID with optional transforms and preview
 */
export async function getDataset(
  datasetId: string,
  options?: {
    includeTransforms?: boolean;
    includePreview?: boolean;
    previewLimit?: number;
  }
): Promise<Dataset> {
  const params = new URLSearchParams();
  if (options?.includeTransforms !== undefined) {
    params.append("include_transforms", String(options.includeTransforms));
  }
  if (options?.includePreview) {
    params.append("include_preview", "true");
    if (options.previewLimit) {
      params.append("preview_limit", String(options.previewLimit));
    }
  }

  const query = params.toString() ? `?${params.toString()}` : "";
  return get<Dataset>(`/api/datasets/${datasetId}${query}`);
}

/**
 * Upload a CSV file to create a dataset (legacy - one-step flow)
 * @deprecated Use uploadFile + createDataset for the two-step flow
 */
export async function uploadDataset(
  projectId: string,
  name: string,
  file: File,
  description?: string
): Promise<DatasetUploadResponse> {
  const fields: Record<string, string> = {
    project_id: projectId,
    name,
  };
  if (description) {
    fields.description = description;
  }

  return uploadFile<DatasetUploadResponse>("/api/datasets/upload", file, fields);
}

