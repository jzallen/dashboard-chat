/**
 * Step 1: Upload a file and get an UploadEvent with inferred schema
 *
 * @param projectId - Project to upload to
 * @param file - CSV file to upload
 * @param datasetId - Optional existing dataset ID (for re-uploads)
 * @returns UploadEvent with schema_config and preview_rows
 */
export async function createUpload(
  projectId: string,
  file: File,
  datasetId?: string
): Promise<UploadEvent> {
  const fields: Record<string, string> = {
    project_id: projectId,
  };
  if (datasetId) {
    fields.dataset_id = datasetId;
  }

  return uploadFile<UploadEvent>("/api/uploads", file, fields);
}

/**
 * Get an upload event by ID
 */
export async function getUpload(
  uploadId: string,
  options?: {
    includePreview?: boolean;
    previewLimit?: number;
  }
): Promise<UploadEvent> {
  const params = new URLSearchParams();
  if (options?.includePreview) {
    params.append("include_preview", "true");
    if (options.previewLimit) {
      params.append("preview_limit", String(options.previewLimit));
    }
  }

  const query = params.toString() ? `?${params.toString()}` : "";
  return get<UploadEvent>(`/api/uploads/${uploadId}${query}`);
}

/**
 * List upload events for a dataset (upload history)
 */
export async function listUploads(options?: {
  projectId?: string;
  datasetId?: string;
}): Promise<UploadEvent[]> {
  const params = new URLSearchParams();
  if (options?.projectId) {
    params.append("project_id", options.projectId);
  }
  if (options?.datasetId) {
    params.append("dataset_id", options.datasetId);
  }

  const query = params.toString() ? `?${params.toString()}` : "";
  return get<UploadEvent[]>(`/api/uploads${query}`);
}

/**
 * Step 2: Create a dataset from an upload with partition configuration
 *
 * @param request - Dataset creation request with upload_id and partition_fields
 * @returns Created dataset with preview rows
 */
export async function createDatasetFromUpload(
  request: DatasetCreateRequest
): Promise<Dataset> {
  return post<Dataset>("/api/datasets", request);
}

/**
 * Update a dataset's metadata
 */
export async function updateDataset(
  datasetId: string,
  data: DatasetUpdate
): Promise<Dataset> {
  return patch<Dataset>(`/api/datasets/${datasetId}`, data);
}

// Transform management — dedicated bulk endpoints, fire-and-forget

/**
 * Batch-create transforms on a dataset
 */
export async function createTransform(
  datasetId: string,
  data: TransformCreate
): Promise<void> {
  await post(`/api/datasets/${datasetId}/transforms`, {
    transforms: [data],
  });
}

/**
 * Batch-update a transform (name, condition, status)
 */
export async function updateTransform(
  datasetId: string,
  transformId: string,
  data: TransformUpdate
): Promise<void> {
  await patch(`/api/datasets/${datasetId}/transforms`, {
    updates: [{ id: transformId, ...data }],
  });
}

/**
 * Soft-delete a transform via status='deleted'
 */
export async function deleteTransform(
  datasetId: string,
  transformId: string
): Promise<void> {
  await patch(`/api/datasets/${datasetId}/transforms`, {
    updates: [{ id: transformId, status: 'deleted' }],
  });
}

/**
 * Toggle a transform's enabled/disabled state
 */
export async function toggleTransform(
  datasetId: string,
  transformId: string,
  enabled: boolean
): Promise<void> {
  await updateTransform(datasetId, transformId, { status: enabled ? 'enabled' : 'disabled' });
}

/**
 * Convert a full Dataset to a DatasetSparse reference for project state
 */
export function datasetToSparse(d: Dataset): DatasetSparse {
  return {
    id: d.id,
    name: d.name,
    link: `/api/datasets/${d.id}`,
    description: d.description,
    schema_config: d.schema_config,
  };
}
