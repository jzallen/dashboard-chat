var SSEStreamWriter = class {
  constructor() {
    this.encoder = new TextEncoder();
    this.accumulatedToolCalls = {};
    const { readable, writable } = new TransformStream();
    this.readable = readable;
    this.writer = writable.getWriter();
  }
  async write(payload) {
    const message2 = `data: ${JSON.stringify(payload)}

`;
    await this.writer.write(this.encoder.encode(message2));
  }
  async writeContent(content) {
    if (!content) return;
    await this.write({ type: "content", content });
  }
  accumulateToolCalls(toolCalls) {
    if (!toolCalls) return;
    for (const tc of toolCalls) {
      const index = tc.index;
      if (!this.accumulatedToolCalls[index]) {
        this.accumulatedToolCalls[index] = {
          id: tc.id || "",
          function: { name: "", arguments: "" }
        };
      }
      if (tc.id) this.accumulatedToolCalls[index].id = tc.id;
      if (tc.function?.name)
        this.accumulatedToolCalls[index].function.name += tc.function.name;
      if (tc.function?.arguments)
        this.accumulatedToolCalls[index].function.arguments += tc.function.arguments;
    }
  }
  async writeToolCalls() {
    const toolCallsArray = Object.values(this.accumulatedToolCalls);
    if (toolCallsArray.length === 0) return;
    await this.write({
      type: "tool_calls",
      tool_calls: toolCallsArray.map((tc) => ({
        id: tc.id,
        type: "function",
        function: tc.function
      }))
    });
  }
  async writeDone() {
    await this.write({ type: "done" });
  }
  async writeError(error) {
    const errorMessage = error instanceof Error ? error.message : "Unknown error";
    await this.write({ type: "error", error: errorMessage });
  }
  async close() {
    await this.writer.close();
  }
};
async function handleChat(request, client) {
  const { messages, tableSchema } = await request.json();
  if (!Array.isArray(messages) || messages.length === 0) {
    return new Response(JSON.stringify({ error: "messages must be a non-empty array" }), {
      status: 400,
      headers: { "Content-Type": "application/json" }
    });
  }
  if (!tableSchema?.columns) {
    return new Response(JSON.stringify({ error: "tableSchema with columns is required" }), {
      status: 400,
      headers: { "Content-Type": "application/json" }
    });
  }
  const systemPrompt = getSystemPrompt(tableSchema);
  const chatMessages = [
    { role: "system", content: systemPrompt },
    ...messages
  ];
  const tools = getToolDefinitions(tableSchema);
  const stream = new SSEStreamWriter();
  streamLLMResponse(
    client,
    { messages: chatMessages, tools },
    stream
  ).catch(console.error);
  return new Response(stream.readable, {
    headers: {
      "Content-Type": "text/event-stream",
      "Cache-Control": "no-cache",
      Connection: "keep-alive"
    }
  });
}
async function streamLLMResponse(client, request, stream) {
  let streamCompleted = false;
  try {
    for await (const chunk of client.streamCompletion(request)) {
      try {
        const parsed = JSON.parse(chunk);
        const delta = parsed.choices?.[0]?.delta;
        const finishReason = parsed.choices?.[0]?.finish_reason;
        if (!delta && !finishReason) continue;
        if (delta) {
          await stream.writeContent(delta.content);
          stream.accumulateToolCalls(delta.tool_calls);
        }
        if (finishReason) {
          await stream.writeToolCalls();
          await stream.writeDone();
          streamCompleted = true;
        }
      } catch (parseError) {
        console.error("[handleChat] Parse error for chunk:", parseError);
      }
    }
    if (!streamCompleted) {
      await stream.writeToolCalls();
      await stream.writeDone();
    }
  } catch (error) {
    await stream.writeError(error);
  } finally {
    await stream.close();
  }
}

